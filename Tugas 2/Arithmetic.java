public class Arithmetic {
    public static void main(String[] args) {
        int a = 25;
        int b = 12;
        int c;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println(a + " + " + b + " = " + (a + b));
        System.out.println(a + " - " + b + " = " + (a - b));
        System.out.println(a + " * " + b + " = " + (a * b));
        System.out.println(a + " / " + b + " = " + ((double)a / b));
        System.out.println(a + " % " + b + " = " + (a % b));
    }
}
